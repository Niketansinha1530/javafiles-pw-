
public class immutableString {

	public static void main(String[] args) {
		
		String name="niketan";
		String surname=new String("sinha");
		char ch[]= {'c','o','d','e','r'};
		String cp=new String (ch);
		System.out.println(name);
		System.out.println(surname);
		System.out.println(cp);
		String s=new String();
	}

}
